    public void MostrarTablaCompras(){
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Cedula");
        modelo.addColumn("Fecha");
        modelo.addColumn("Vendedor");
        modelo.addColumn("Tipo_de_compra");
        modelo.addColumn("Articulo");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");
        tablacompras.setModel(modelo);
        
          Thread thread = new Thread(() -> {
        while (true) {
            try {
                Conexion co = new Conexion();
                ArrayList<Compras> lista = co.ListarCompras();
                
               
                modelo.setRowCount(0);
                
               
                for (Compras compra : lista) {
                    Object[] datos = {
                        compra.getId(),
                        compra.getCedula(),
                        compra.getFecha(),
                        compra.getVendedor(),
                        compra.getTipo(),
                        compra.getArticulo(),
                        compra.getCantidad(),
                        compra.getPrecio()
                    };
                    modelo.addRow(datos);
                }
                
                
                Thread.sleep(1000); 
            } catch (InterruptedException e) {
                e.printStackTrace();
            }
        }
    });
    
   
    thread.start();
    
    
       
    }
    
    
    
    
