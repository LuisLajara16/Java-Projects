    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String cedula = txtcedula.getText();
    String fecha = txtfecha.getText();
    String vendedor = (String) jComboBox1.getSelectedItem();
    String tipo = (String) jComboBox2.getSelectedItem();
    String articulo = (String) jComboBox3.getSelectedItem();
    String cantidadStr = txtcantidad.getText();
    String precioStr = txtprecio.getText();

   
    try {
        double cantidad = Double.parseDouble(cantidadStr);
        double precio = Double.parseDouble(precioStr);
        

        if (cantidad < 0 || precio < 0) {
            JOptionPane.showMessageDialog(null, "La cantidad y el precio no pueden ser negativos");
            return; 
        }
        
       
        Conexion co = new Conexion();
        int respuesta = co.Reusuario2(cedula, fecha, vendedor, tipo, articulo, cantidadStr, precioStr);
        if (respuesta == 1) {
            JOptionPane.showMessageDialog(null, "Compra registrada correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "Error al registrar compra");
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "La cantidad y el precio deben ser números válidos");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

