    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here
         String id = txtid.getText(); 
    
  
    if (!id.isEmpty()) {
        Conexion co = new Conexion();
        Compras compra = co.BuscarCompra(id); 
        
        
        if (compra != null) {
        
            txtcedula.setText(compra.getCedula());
            txtfecha.setText(compra.getFecha());
            jComboBox1.setSelectedItem(compra.getVendedor());
            jComboBox2.setSelectedItem(compra.getTipo());
            jComboBox3.setSelectedItem(compra.getArticulo());
            txtcantidad.setText(compra.getCantidad());
            txtprecio.setText(compra.getPrecio());
        } else {
            
            JOptionPane.showMessageDialog(null, "No se encontró una compra con el ID especificado");
        }
    } else {
        
        JOptionPane.showMessageDialog(null, "Por favor ingresa un ID antes de buscar");
    }
        
    }//GEN-LAST:event_jButton5ActionPerformed

